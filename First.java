public class First {

    public static void main(String[] args) {
        System.out.println("Hello world");
        System.out.print("Hello world"); //doesn't add a new line automatically
        System.out.println();
        System.out.println("Hello \\ world"); // to add a \
        System.out.println("Hello \" world"); // to add a "
        System.out.println("Hello \t world"); // to add a tab
        System.out.println("Hello \n world"); // to add a next line

    }

}